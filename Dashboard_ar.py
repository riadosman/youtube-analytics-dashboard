@st.cache_data
def load_Data():

    # ========================================================
    # قراءة ملف البيانات الرئيسي
    # ========================================================

    df_agg = pd.read_csv(
        "Aggregated_Metrics_By_Video.csv"
    ).iloc[1:, :]

    # iloc[1:, :]
    #
    # 1:
    # تجاهل أول صف
    #
    # :
    # جميع الأعمدة


    # ========================================================
    # تنظيف أسماء الأعمدة
    # ========================================================

    df_agg.columns = (

        df_agg.columns

        # حذف محرف مخفي يسمى Soft Hyphen
        # وجوده قد يجعل اسم العمود يبدو صحيحًا
        # لكنه مختلف فعليًا.
        .str.replace("\xad", "", regex=False)

        # إزالة الفراغات من بداية ونهاية اسم العمود.
        .str.strip()

    )


    # ========================================================
    # قراءة ملف الدول والمشتركين
    # ========================================================

    df_agg_sub = pd.read_csv(
        "Aggregated_Metrics_By_Country_And_Subscriber_Status.csv"
    )


    # ========================================================
    # قراءة ملف التعليقات
    # ========================================================

    df_comments = pd.read_csv(
        "Aggregated_Metrics_By_Video.csv"
    )


    # ========================================================
    # قراءة ملف الأداء مع الزمن
    # ========================================================

    df_time = pd.read_csv(
        "Video_Performance_Over_Time.csv"
    )


    # ========================================================
    # طباعة أسماء الأعمدة للتأكد من صحتها
    # ========================================================

    print(df_agg.columns.tolist())


    # ========================================================
    # تحويل مدة المشاهدة إلى datetime
    # ========================================================

    df_agg["Average view duration"] = (

        df_agg["Average view duration"]

        .apply(

            lambda x:

            datetime.strptime(
                x,
                "%H:%M:%S"
            )

        )

    )

    # مثال

    # قبل التحويل
    # "00:03:15"

    # بعد التحويل
    # datetime.time object


    # ========================================================
    # إنشاء عمود جديد بالثواني
    # ========================================================

    df_agg["Avg_duration_sec"] = (

        df_agg["Average view duration"]

        .apply(

            lambda x:

            x.second
            + x.minute * 60
            + x.hour * 3600

        )

    )

    # مثال

    # 00:03:15

    # second = 15
    # minute = 3

    # الناتج

    # 15 + 180

    # =195 ثانية


    # ========================================================
    # حساب Engagement Ratio
    # ========================================================

    df_agg["Engagement_ratio"] = (

        (

            df_agg["Comments added"]

            + df_agg["Shares"]

            + df_agg["Dislikes"]

            + df_agg["Likes"]

        )

        /

        df_agg["Views"]

    )

    # هذا يمثل

    # (Likes + Comments + Shares + Dislikes)

    # --------------------------------------

    #             Views

    # أي نسبة التفاعل إلى عدد المشاهدات.


    # ========================================================
    # حساب عدد المشاهدات اللازمة للحصول على مشترك واحد
    # ========================================================

    df_agg["Views / sub gained"] = (

        df_agg["Views"]

        /

        df_agg["Subscribers gained"]

    )

    # كلما كانت القيمة أصغر
    # كان الفيديو أكثر كفاءة في جذب المشتركين.


    # ========================================================
    # ترتيب الفيديوهات
    # ========================================================

    df_agg.sort_values(

        "Video publish time",

        ascending=False,

        inplace=True

    )

    # ascending=False

    # يعني الترتيب من الأحدث إلى الأقدم.


    # ========================================================
    # تحويل تاريخ النشر إلى Timestamp
    # ========================================================

    df_agg["Video publish time"] = pd.to_datetime(

        df_agg["Video publish time"],

        format="mixed",

        errors="coerce"

    )

    # format="mixed"

    # يجعل Pandas يحاول اكتشاف صيغة التاريخ تلقائيًا.

    # errors="coerce"

    # إذا وجد تاريخًا خاطئًا
    # يحوله إلى NaT
    # بدلًا من إيقاف البرنامج.


    # ========================================================
    # تحويل تواريخ ملف الأداء الزمني
    # ========================================================

    df_time["Date"] = pd.to_datetime(

        df_time["Date"],

        format="mixed",

        errors="coerce",

        dayfirst=True

    )

    # dayfirst=True

    # يعني أن التاريخ مكتوب بالشكل

    # 15/08/2023

    # وليس

    # 08/15/2023


    # ========================================================
    # إعادة جميع DataFrames
    # ========================================================

    return (

        df_agg,

        df_agg_sub,

        df_comments,

        df_time

    )

