# ============================================================

def style_negative(v, props=""):
    """
    Parameters
    ----------
    v
        القيمة الموجودة داخل الخلية.

    props
        تنسيق CSS مثل:
        color:red;

    Returns
    -------
    إذا كانت القيمة سالبة يرجع التنسيق.
    وإذا لم تكن سالبة يرجع None.
    """

    try:

        # إذا كانت القيمة أصغر من صفر
        # سيتم إرجاع خصائص التنسيق.
        return props if v < 0 else None

    except:

        # إذا كانت القيمة ليست رقمًا
        # مثل النصوص أو التواريخ
        # فلن يحدث خطأ.
        pass


# ============================================================
# دالة تلوين القيم الموجبة
# ============================================================

def style_positive(v, props=""):
    """
    نفس الدالة السابقة ولكن للقيم الموجبة.
    """

    try:

        # إذا كانت القيمة أكبر من صفر
        # سيتم تطبيق اللون الأخضر.
        return props if v > 0 else None

    except:
        pass


